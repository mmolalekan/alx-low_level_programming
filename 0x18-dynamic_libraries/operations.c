/*
* add - adds two integers
* @a: operand 1
* @b: operand b
* Return: the summation
*/

int add(int a, int b)
{
	return (a + b);
}

/*
* sub - computes the difference between two integers
* @a: operand 1
* @b: operand b
* Return: the difference
*/

int sub(int a, int b)
{
	return (a - b);
}

/*
* mul - multiplies two integers
* @a: operand 1
* @b: operand b
* Return: the poduct
*/

int mul(int a, int b)
{
	return (a * b);
}

/*
* div - divides two integers
* @a: operand 1
* @b: operand b
* Return: the division
*/

int div(int a, int b)
{
	return (a / b);
}

/*
* mod - find the modulus of two integers
* @a: operand 1
* @b: operand b
* Return: the modulus
*/

int mod(int a, int b)
{
	return (a % b);
}
